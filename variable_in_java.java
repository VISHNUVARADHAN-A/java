public class variable_in_java {
    public static void main(String[] args) {
        int integer_value=42;
        System.out.println(integer_value);
        
        short short_value=93;
        System.out.println(short_value);

        long  long_value=1234567;
        System.out.println(long_value);

        double double_value=7.12345678;
        System.out.println(double_value);

        float float_value=5.2345678f;
        System.out.println(float_value);

        char charter_value='a';
        System.out.println(charter_value);

        boolean Boolean_value=true;
        System.out.println(Boolean_value);

        byte  byte_value=12;
        System.out.println(byte_value);





    }
}
